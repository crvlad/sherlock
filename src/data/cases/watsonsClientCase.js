  evaluateAccusation(answers, leadsVisited) {
    const killerText = normalizeText(answers?.killer);
    const motiveText = normalizeText(answers?.motive);
    const methodText = normalizeText(answers?.method);

    const killerMatch = ['вестон', 'генри', 'weston', 'henry'].some(token => killerText.includes(token));
    const motiveMatch = ['ревност', 'любов', 'завист', 'конкуренц'].some(token => motiveText.includes(token));
    const methodMatch = ['нож', 'удар', 'закол', 'клинок', 'stab'].some(token => methodText.includes(token));

    const allCorrect = killerMatch && motiveMatch && methodMatch;
    const isCorrect = killerMatch;
    const score = calculateWeightedScore({
      leadsVisited,
      sherlockLeadCount: this.scenario.sherlockLeadCount,
      killerMatch,
      motiveMatch,
      methodMatch,
    });

    return {
      isCorrect,
      score,
      killerMatch,
      motiveMatch,
      methodMatch,
      allCorrect,
      confidence: (killerMatch ? 2 : 0) + (motiveMatch ? 1 : 0) + (methodMatch ? 1 : 0),
    };
  },
