export const EMERALD_CONSPIRACY_CASE = {
  id: scenario.id,
  scenario,
  locations,
  leads,
  newspapers,
  directory,
  evaluateAccusation(answers, leadsVisited) {
    const killerText = normalizeText(answers?.killer);
    const motiveText = normalizeText(answers?.motive);
    const methodText = normalizeText(answers?.method);

    const killerMatch = ['уэйлен', 'брюс', 'waylen'].some(alias => killerText.includes(alias));
    const motiveMatch = ['патент', 'кража', 'наследств', 'разоблачен', 'отец', 'скрыт'].some(token => motiveText.includes(token));
    const methodMatch = ['кураре', 'яд', 'игл', 'электр', 'отрав', 'ток'].some(token => methodText.includes(token));

    const confidence = (killerMatch ? 2 : 0) + (motiveMatch ? 1 : 0) + (methodMatch ? 1 : 0);
    const isCorrect = killerMatch;
    const score = calculateWeightedScore({
      leadsVisited,
      sherlockLeadCount: scenario.sherlockLeadCount,
      killerMatch,
      motiveMatch,
      methodMatch,
    });

    return {
      isCorrect,
      score,
      confidence,
      killerMatch,
      motiveMatch,
      methodMatch,
    };
  },
};
